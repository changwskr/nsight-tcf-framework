    static void mergeAll(JdbcTemplate jdbcTemplate) {
        for (Entry seed : ALL) {
            jdbcTemplate.update("""
                    MERGE INTO OM_SERVICE_CATALOG (CATALOG_ID, BUSINESS_CODE, SERVICE_ID, TRANSACTION_CODE,
                                                   PROCESSING_TYPE, HANDLER_CLASS, AUTH_CODE, AUDIT_YN,
                                                   TIMEOUT_SEC, USE_YN, DESCRIPTION)
                    KEY (CATALOG_ID)
                    VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
                    """,
                    seed.catalogId(), seed.businessCode(), seed.serviceId(), seed.transactionCode(),
                    seed.processingType(), seed.handlerClass(), seed.authCode(), seed.auditYn(),
                    seed.timeoutSec(), seed.useYn(), seed.description());
        }
    }

